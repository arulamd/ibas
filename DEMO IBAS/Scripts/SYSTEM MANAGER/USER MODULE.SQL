SELECT 
    m.MODULECODE,
    m.MODULENAME,
    u.RIGHTSCODE,
    u.RIGHTSNAME
FROM 
    MODULEMASTER m,
    RIGHTSMASTER u,
    USERRIGHTSMASTER f,
    USERMASTER g
WHERE 
    m.MODULECODE = u.MODULE
    AND u.COMPANYCODE = 'COMCL' 
    AND f.RIGHTSCODE = u.RIGHTSCODE 
    AND f.COMPANYCODE = u.COMPANYCODE
    AND f.USERCODE = g.USERCODE
    AND g.USERNAME = 'PARDO'
GROUP BY 
    m.MODULECODE,
    m.MODULENAME,
    u.RIGHTSCODE,
    u.RIGHTSNAME
ORDER BY 
    m.MODULECODE,
    u.RIGHTSCODE;




